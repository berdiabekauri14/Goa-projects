// Objective: Create a form with multiple input fields (e.g., name, email, and message). Add a "Clear" button that, when clicked, clears all input fields.

let form = document.getElementById('form')
let nameValue = form.type.value
let ageValue = form.numbers.value
let emailValue = form.email.value
let passwordValue = form.password.value

function clearInputValues() {
    nameValue == ''
    ageValue == ''
    emailValue == ''
    passwordValue == ''
}