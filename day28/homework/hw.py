#განიხილე ეს კოდი კომენტარებით (მე მომცეს მარტო)

def square_digits(num): #ჯერ შევქმენით ფუნქცია square_digits და num პარამეტრი
    result = '' #მერე შეიქმნა result ცვლადი სადაც იქნება ხარისხების პასუხი
    
    for char in str(num): #გაკეთდა for ცილკი და num პარამეტრი გახდა სტრინგი
        result += str(int(char) ** 2) #მიუმატეთ result ცვლადს ციფრის ხარისხი მაგალითად: "5 ** 2 = 5 * 5 = 25" და მერე გადაეცემა result ცვლადს ინტეჯერად
    
    return int(result) #დააბრუნებს result ცვლადს ინტეჯერად