# min()

x = min(5, 10)
print(x)

y = min(10, 5)
print(y)

a = min(1, 5, 10)
print(a)

b = min(10, 5, 1)
print(b)

c = min('berdia', 'nika', 'davit')
print(c)

d = min('davit', 'nika', 'berdia')
print(d)

# max()

m = max(5, 10)
print(m)

n = max(10, 5)
print(n)

r = max(1, 10, 100)
print(r)

k = max(100, 10, 1)
print(k)

i = max('berdia', 'nika', 'davit')
print(i)

q = max('davit', 'nika', 'berdia')
print(q)