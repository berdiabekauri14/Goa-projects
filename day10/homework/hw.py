
#0-იდან 20-ის ჩათვლით გამოიტანეთ რიცხვები.
for i in range(0,20):
    print(i)

#შექმენით პროგრამა, რომელიც ბეჭდავს 5-ის ჯერადებს 1-დან 50-ის ჩათვლით for loop-ის გამოყენებით.
for i in range(1,50):
    if i % 5:
        print(i)

for i in range(0,20):
    if i % 2:
        print(i)

for i in range(50,100):
    print(i)

#0-იდან 50-ის ჩათვლით გამოიტანეთ რიცხვები და ბოლოს მათი ჯამი.
for i in range(0,50):
    i += i
print(i)
